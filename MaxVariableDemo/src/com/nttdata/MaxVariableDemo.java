package com.nttdata;

public class MaxVariableDemo {
	public static void main(String[] args) {
		
	
		byte largestByte= Byte.MAX_VALUE;
		byte smallestByte= Byte.MIN_VALUE;
		short largestShort= Short.MAX_VALUE;
		short smallestShort= Short.MIN_VALUE;
		int largestInt= Integer.MAX_VALUE;
		int smallestInt= Integer.MIN_VALUE;
		long largestLong= Long.MAX_VALUE;
		long smallestLong = Long.MIN_VALUE;
		float largestFloat = Float.MAX_VALUE;
		float smallestFloat = Float.MIN_VALUE;
		double largestDouble = Double.MAX_VALUE;
		double smallestDouble= Double.MIN_VALUE;
		
		
		boolean aBoolean = false;
		
		System.out.println("largestByte= "+ largestByte);
		System.out.println("SmallestByte= "+ smallestByte);
		System.out.println("largestShort= "+largestShort);
		System.out.println("Smallest Short= "+smallestShort);
		System.out.println("Largest Int= "+largestInt);
		System.out.println("Smallest Int= "+smallestInt);
		System.out.println("Largest Long= "+largestLong);
		System.out.println("Smallest Long= "+smallestLong);
		System.out.println("Largest Float= "+largestFloat);
		System.out.println("Smallest Float= "+smallestFloat);
		System.out.println("Largest Double= "+largestDouble);
		System.out.println("smallest Double= "+smallestDouble);
		System.out.println("Boolean value= "+aBoolean);
		
		
	}
}
