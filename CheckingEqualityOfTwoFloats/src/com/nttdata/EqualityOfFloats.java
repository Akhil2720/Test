package com.nttdata;

public class EqualityOfFloats {

	public static void main(String[] args) {
		float num1=1.5f;
		float num2=1.527585659f;
		float compare=num1-num2;
		if(compare == 0) System.out.println("Both The Float numbers are same.");
		else System.out.println("Both The Float numbers are not same.");
	}
}
